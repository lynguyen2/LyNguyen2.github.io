/**
 * OPERATORS
 * 
 * 0. Javascript operators are symbols used to help execute our data. They can manipulate, 
 * assign, compare values and more. The 6 types of operators are: assignment operators, 
 * arithmetic operators, comparison operators, logical operators, unary operators, and 
 * ternary operators.
 * 
 * 1. Operands in operators can involve one, two, or many values, therefore they're 
 * classified as such: One - Unary; Two - Binary; or Many - Ternary
**/
//  1. Assignment Operators (Unary) - Assigns a value(s) to a variable(s)

//  =	assignment for initilizing variables 
    var number = 10; // set number equal to 10
    
//  +=	means x = x + y
    number += 5; // add 5 to the current value of number = 10
    
// -= means x = x - y
    5 -+ 2; // = 3
    
// *= means x = x * y
    4 * 3; // = 12
    
// /= means x = x / y
    12 / 4; // = 3
    
//   2. Arithmetic Operators (Binary) - Perform basic mathemathics

//   +	addition
    10 + 1; // = 11
    
//  -	subtraction
    2 - 1; // = 1
    
//  *	multiplication
    4 * 4; // = 16
    
//  /	division
    20 / 5; // = 4
    
//  %	modulo (remainder of division) means x = x % y
    13 % 4; // = 1
 
//  3.  Comparison Operators (Binary) -  Perform comparisons and returns a Boolean value
//  >   greater than
    7 > 4; // prints true
    
//  <   less than
    7 < 4; // prints false
    
//  >=  greater than or equal to
    10 >= 8; // prints true
    
//  <=  less than or equal to
    5 <= 5; // prints true
    
//  === strict comparison
    1 === 1; // prints true
    
//  ==  non-strict comparison
    2 == '2'; // prints true
 
//  4. Logical Operators (Binary) - and / or

//  &&  means and
    true && true // print true
    
//  ||  means or
    true || false // print true
 
//  5. Unary Opertor - an operation with only one operand
 
//  typeof returns data type as string
    typeof true // returns boolean
    
//  ! bang operater means "not" 
    "2" !== 4; // 2 isn't not equal to 4
 
