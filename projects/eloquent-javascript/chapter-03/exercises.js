function min(num1, num2) {
  return Math.min(num1, num2);

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
      if (n === 0)  {
        return true;
    } else if (n === 1) {
        return false;
    } else if (n < 0) {
        return isEven(-n);
    } else {
        return isEven(n - 2);
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  var charCount = 0;
  for (var i = 0; i < str.length; i++) {
    var letterC = str.charAt(i);
    if (letterC === char) {
      charCount++;
    }
  }
  return charCount;
};

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var bCount = 0;
  for (var i = 0; i < str.length; i++) {
    var letterC = str.charAt(i);
    if (letterC === "B") {
      bCount++;
    }
  }
  return bCount;
};

